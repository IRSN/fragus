def _is_paraphrase(val) -> bool:
    """Robust boolean check for is_paraphrase — handles True/False, 1.0/0.0, 'true'/'false'."""
    if val is None:
        return False
    if isinstance(val, float) and math.isnan(val):
        return False
    if isinstance(val, (bool, int, float)):
        return bool(val)
    return str(val).strip().lower() in ("true", "1")


def extract_cited_files(response: str, chunks: list[dict]) -> list[str]:
    """Parse [N] citations and return unique source file names in citation order."""
    seen_idx: list[int] = []
    for m in re.finditer(r"\[(\d+)\]", response):
        idx = int(m.group(1))
        if idx not in seen_idx:
            seen_idx.append(idx)
    cited: list[str] = []
    seen_names: set[str] = set()
    for idx in seen_idx:
        if 1 <= idx <= len(chunks):
            name = chunks[idx - 1].get("name") or chunks[idx - 1].get("path") or ""
            if name and name not in seen_names:
                cited.append(name)
                seen_names.add(name)
    return cited


def parse_reference_files(cell_value) -> list[str]:
    """Parse reference file names from an Excel cell (JSON list or newline-separated)."""
    if cell_value is None or (isinstance(cell_value, float) and math.isnan(cell_value)):
        return []
    s = str(cell_value).strip()
    if not s:
        return []
    try:
        parsed = json.loads(s)
        if isinstance(parsed, list):
            return [str(x).strip() for x in parsed if str(x).strip()]
    except Exception:
        pass
    return [x.strip() for x in s.splitlines() if x.strip()]


class _QueryEmbeddingCache:
    """Disk cache for query vectors: {text → {field → vector}}.

    Questions and variants are fixed — their vectors never change regardless of
    the retrieval config (weights, collection). Persist once, reuse across all
    experiments. Only texts missing a required field are re-encoded.
    """

    def __init__(self, path: Path) -> None:
        self._path = path
        self._store: dict[str, dict] = {}
        if path.exists():
            try:
                with open(path, "rb") as fh:
                    self._store = pickle.load(fh)
                print(f"[query_cache] {len(self._store)} texts loaded from {path}")
            except Exception as e:
                print(f"[query_cache] Could not load cache ({e}) — starting fresh.")

    def get(self, text: str, needed_fields: set) -> dict | None:
        """Return cached vectors for all needed fields, or None if any is missing."""
        entry = self._store.get(text)
        if entry and needed_fields.issubset(entry.keys()):
            return {f: entry[f] for f in needed_fields}
        return None

    def update_batch(self, encoded: dict) -> None:
        """Merge new {text → {field → vector}} into the store and persist."""
        for text, vecs in encoded.items():
            if text in self._store:
                self._store[text].update(vecs)
            else:
                self._store[text] = dict(vecs)
        self._path.parent.mkdir(parents=True, exist_ok=True)
        with open(self._path, "wb") as fh:
            pickle.dump(self._store, fh)


def _run_rag(rag: HybridRAGPipeline, q_id: str, question: str,
             precomputed: dict | None = None) -> dict | None:
    """Run the RAG pipeline; returns the result dict or None on error."""
    try:
        return rag.ask(question, precomputed=precomputed)
    except Exception as exc:
        tqdm.write(f"  ERROR RAG [{q_id}] '{question[:70]}': {exc}")
        return None


def _fill_rag_result(row: dict, result: dict | None) -> None:
    """Populate response/retrieved_contexts/retrieved_files/cited_files in-place from a RAG result.

    ``retrieved_contexts`` holds the FULL chunk texts (no truncation). Use
    :func:`save_eval_outputs` to persist: full contexts go to a parquet sidecar
    (read by rag_evaluation.py), while the Excel copy is truncated per chunk to
    fit Excel's 32 767-char cell limit.
    """
    if result is None:
        return
    chunks = result["chunks"]
    row["response"] = result["answer"]
    row["retrieved_contexts"] = json.dumps(
        [c.get("text") or "" for c in chunks], ensure_ascii=False
    )
    # Reranker scores and chunk file names aligned with retrieved_contexts —
    # let threshold variants be derived (filter + generation only) without
    # replaying retrieval, including cited_files/retrieved_files extraction.
    row["rerank_scores"] = json.dumps(
        [c.get("rerank_score") for c in chunks]
    )
    row["chunk_names"] = json.dumps(
        [c.get("name") or c.get("path") or "" for c in chunks], ensure_ascii=False
    )
    # Files present in the retrieved chunks (deduped, order-preserving)
    seen: set[str] = set()
    retrieved_file_names: list[str] = []
    for c in chunks:
        name = c.get("name") or c.get("path") or ""
        if name and name not in seen:
            retrieved_file_names.append(name)
            seen.add(name)
    row["retrieved_files"] = json.dumps(retrieved_file_names, ensure_ascii=False)
    row["cited_files"] = json.dumps(
        extract_cited_files(result["answer"], chunks), ensure_ascii=False
    )


# ---------------------------------------------------------------------------
# Output persistence: Excel (human-readable, truncated) + parquet (full contexts)
# ---------------------------------------------------------------------------

_EXCEL_CELL_LIMIT = 30_000  # Excel hard cap is 32 767 chars/cell; stay safely below


def contexts_parquet_path(output_path: Path) -> Path:
    """Sidecar parquet holding the FULL retrieved contexts (no Excel cell limit)."""
    return output_path.with_name(output_path.stem + "_contexts.parquet")


def _truncate_contexts_cell(value):
    """Shrink a retrieved_contexts JSON cell so it fits in one Excel cell.

    Chunks are truncated individually so the cell stays VALID JSON — the Excel
    copy remains parseable (fallback when no parquet sidecar exists), just
    shortened. The parquet sidecar keeps the full version.
    """
    if not isinstance(value, str) or len(value) <= _EXCEL_CELL_LIMIT:
        return value
    try:
        texts = json.loads(value)
    except Exception:
        return value[:_EXCEL_CELL_LIMIT]
    budget = _EXCEL_CELL_LIMIT - 8 * len(texts) - 2  # JSON overhead per entry + brackets
    per_chunk = max(200, budget // max(1, len(texts)))
    return json.dumps([str(t)[:per_chunk] for t in texts], ensure_ascii=False)


def save_eval_outputs(rows: dict, output_path: Path) -> None:
    """Persist eval rows: parquet sidecar with full contexts + Excel with truncated ones."""
    df = pd.DataFrame(list(rows.values()))
    if "retrieved_contexts" in df.columns:
        pq_cols = ["q_id", "retrieved_contexts"] + [
            c for c in ("rerank_scores", "chunk_names") if c in df.columns
        ]
        pq = df[df["retrieved_contexts"].notna()][pq_cols]
        if len(pq):
            pq.to_parquet(contexts_parquet_path(output_path), index=False)
        df = df.copy()
        df["retrieved_contexts"] = df["retrieved_contexts"].map(_truncate_contexts_cell)
    df.to_excel(output_path, index=False)


def load_full_contexts(rows: dict, output_path: Path) -> None:
    """On resume: restore full contexts from the parquet sidecar over the truncated Excel ones."""
    pq_path = contexts_parquet_path(output_path)
    if not pq_path.exists():
        return
    full = pd.read_parquet(pq_path)
    by_qid = dict(zip(full["q_id"].astype(str), full["retrieved_contexts"]))
    for qid, row in rows.items():
        if qid in by_qid:
            row["retrieved_contexts"] = by_qid[qid]


