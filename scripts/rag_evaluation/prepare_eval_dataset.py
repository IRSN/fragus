_PARAPHRASE_PROMPT = """\
Tu es un expert en reformulation de questions techniques et réglementaires.

Génère {n} reformulations de la question suivante. Chaque reformulation doit :
- Poser exactement la même question au fond, sans en changer le sens ni la portée
- Avoir une structure de phrase distincte de l'originale et des autres reformulations
- Varier le vocabulaire (synonymes, tournures alternatives) sans déformer le sens technique
- Adopter un angle légèrement différent : plus direct, plus contextuel, sous forme d'une demande d'explication, etc.

Ne génère PAS de questions différentes — uniquement des façons alternatives de poser la même question.

Question originale : {question}

Réponds UNIQUEMENT avec un objet JSON valide, sans Markdown ni texte avant/après :
{{"reformulations": ["reformulation 1", ...]}}"""


def _build_paraphrase_llm(proxy_prefix: str):
    """Lightweight ChatOpenAI for paraphrase generation (ALBERT first for speed)."""
    from langchain_openai import ChatOpenAI
    model_key = "mistral-small-3.2-24b-instruct-2506"
    entries = _resolve_all_providers_for_key(proxy_prefix, model_key)
    if not entries:
        raise RuntimeError(f"Paraphrase model '{model_key}' not found on proxy {proxy_prefix}")
    e = entries[0]  # albert comes first
    return ChatOpenAI(
        base_url=e["endpoint"], model=e["name"], api_key="dummy",
        max_tokens=512, timeout=30.0, max_retries=0,
    )


def _generate_paraphrases(llm, question: str, n: int) -> list[str]:
    """Generate n reformulations; returns fewer on LLM/parse failure."""
    try:
        out = llm.invoke(_PARAPHRASE_PROMPT.format(n=n, question=question))
        text = getattr(out, "content", str(out)).strip()
        text = re.sub(r"^```(?:json)?\s*|\s*```$", "", text, flags=re.MULTILINE).strip()
        data = json.loads(text)
        return [str(r).strip() for r in data.get("reformulations", []) if str(r).strip()][:n]
    except Exception as exc:
        tqdm.write(f"  WARN paraphrase generation failed: {exc}")
        return []


