"""
prepare_eval_dataset.py
=======================

Runs HybridRAGPipeline on a question bank (Excel) and produces the input file
expected by rag_evaluation.py.

Optionally generates N paraphrase variants per question (--paraphrase-variants)
to enable the paraphrase_robustness metric. Variants are tagged with
is_paraphrase=True; rag_evaluation.py only scores original questions and uses
the variants solely to compute the stability score.

Input Excel columns (canonical names — same as output):
  q_id                  question identifier
  user_input            question text
  reference             expected answer
  reference_files       JSON list of expected source document names
  test_intent           intent category (optional)
  paraphrase_group_id   shared id for the reformulation group (optional)
  is_paraphrase         False for original, True for pre-existing variants (optional)

  Pre-existing paraphrase variants (is_paraphrase=True rows) in the input are reused
  as-is; only missing variants (count < --paraphrase-variants) are generated via LLM.

Output Excel columns (canonical RAGAS names for rag_evaluation.py):
  q_id                  original question identifier
  test_intent           intent category (kept from input)
  paraphrase_group_id   shared id for question + its reformulations (only if variants > 0)
  is_paraphrase         False for original, True for generated variants
  user_input            question text (original or reformulation)
  response              LLM answer generated by the RAG pipeline
  reference             expected answer (same for all rows in a group)
  retrieved_contexts    JSON list of chunk texts passed to the LLM (after reranking).
                        Truncated per chunk in the Excel (32 767-char cell limit);
                        the FULL version lives in the parquet sidecar
                        eval_test_{experiment_id}_contexts.parquet (q_id,
                        retrieved_contexts, rerank_scores) which rag_evaluation.py
                        reads in priority.
  rerank_scores         JSON list of cross-encoder scores aligned with
                        retrieved_contexts (enables deriving threshold variants
                        without replaying retrieval)
  reference_files       JSON list of expected source document names
  cited_files           JSON list of document names cited via [N] in the response
  retrieved_files       JSON list of unique document names present in the retrieved chunks

Usage:
  python prepare_eval_dataset.py questions.xlsx
  python prepare_eval_dataset.py questions.xlsx --experiment-id baseline_01
  python prepare_eval_dataset.py questions.xlsx --paraphrase-variants 2
  python prepare_eval_dataset.py questions.xlsx --paraphrase-variants 0   # disable
"""

