def main() -> None:
    ap = argparse.ArgumentParser(
        description="Run HybridRAGPipeline on a question bank and produce the rag_evaluation.py input file."
    )
    ap.add_argument("input", help="Input .xlsx with the question bank")
    ap.add_argument("--experiment-id", dest="experiment_id", default=None)
    ap.add_argument("--sheet", default=0)
    ap.add_argument("--nrows", type=int, default=None)
    ap.add_argument("--reranker", default=RERANKER_MODEL)
    ap.add_argument("--candidates", type=int, default=CANDIDATES)
    ap.add_argument("--top-n", type=int, default=TOP_N, dest="top_n")
    ap.add_argument("--collection", default=COLLECTION_NAME)
    ap.add_argument("--dense-weight",  type=float, default=0.5, dest="dense_weight",
                    help="Weight of the dense BGE-M3 field in WeightedRanker (default: 0.5).")
    ap.add_argument("--bm25-weight",   type=float, default=0.5, dest="bm25_weight",
                    help="Weight of the sparse BM25 field in WeightedRanker (default: 0.5).")
    ap.add_argument("--fermi-weight",  type=float, default=0.0, dest="fermi_weight",
                    help="Weight for sparse_fermi field (0 = désactivé, défaut: 0).")
    ap.add_argument("--ranker", default="weighted", choices=["weighted", "rrf"], dest="ranker",
                    help="Fusion ranker: 'weighted' (défaut) ou 'rrf' (Reciprocal Rank Fusion).")
    ap.add_argument("--rrf-k", type=int, default=60, dest="rrf_k",
                    help="Constante de lissage pour RRFRanker (défaut: 60).")
    ap.add_argument(
        "--ranking-mode", default="reranker", choices=["reranker", "combined"],
        dest="ranking_mode",
        help="Classement final après reranking : 'reranker' (score cross-encoder seul, défaut) "
             "ou 'combined' (moyenne normalisée score Milvus + reranker).",
    )
    ap.add_argument(
        "--llm-model-key", default=None, dest="llm_model_key",
        help="Generation LLM model key (Cleyrop proxy). Default: value from scripts/rag/config.toml.",
    )
    ap.add_argument(
        "--paraphrase-variants", type=int, default=3, dest="paraphrase_variants",
        metavar="N",
        help=(
            "Paraphrase variants to generate per question (default: 3). "
            "N=2 → 3 pairs per group (minimum meaningful); "
            "N=3 → 6 pairs (recommended for production). "
            "Use 0 to disable."
        ),
    )
    _default_cache = str(Path(__file__).parent / "data" / "query_embeddings_cache.pkl")
    ap.add_argument(
        "--query-cache", default=_default_cache, dest="query_cache",
        metavar="PATH",
        help=(
            "Path to the query embedding cache (.pkl). Questions and variants are "
            "fixed — their vectors are computed once and reused across all experiments. "
            "Only texts missing a required field are re-encoded. "
            f"Default: {_default_cache}"
        ),
    )
    ap.add_argument(
        "--no-query-cache", action="store_true", dest="no_query_cache",
        help="Disable the query embedding cache (always re-encode).",
    )
    ap.add_argument(
        "--reranker-threshold", type=float, default=None, dest="reranker_threshold",
        metavar="SCORE",
        help=(
            "Minimum reranker score to include a chunk in the context (default: no filter). "
            "Chunks scoring below this threshold are dropped even if they rank within top-n. "
            "Accepts any float, including negative values."
        ),
    )
    ap.add_argument(
        "--contexts-parquet", default=None, dest="contexts_parquet",
        metavar="PATH",
        help=(
            "Generation-only mode: skip retrieval and load chunks (text/name/rerank_score) "
            "from an existing contexts parquet (eval_test_{exp}_contexts.parquet of a "
            "previous run covering the same questions, e.g. produced with a lower or no "
            "reranker threshold). --reranker-threshold is applied to the stored scores. "
            "Rows missing from the parquet are skipped with a warning."
        ),
    )
    args = ap.parse_args()

    _results_dir = Path(__file__).parent / "data" / "results"
    _results_dir.mkdir(parents=True, exist_ok=True)
    experiment_id = args.experiment_id or Path(args.input).stem
    output_path = _results_dir / f"eval_test_{experiment_id}.xlsx"
    n_variants = max(0, args.paraphrase_variants)

    # --- Load question bank ---
    sheet = int(args.sheet) if str(args.sheet).isdigit() else args.sheet
    df_in = pd.read_excel(args.input, sheet_name=sheet, nrows=args.nrows)
    print(f"Loaded {len(df_in)} rows from '{args.input}'")

    for col in (COL_QID, COL_QUESTION, COL_REFERENCE, COL_REF_FILES):
        if col not in df_in.columns:
            raise KeyError(f"Required column '{col}' not found. Available: {list(df_in.columns)}")

    # Separate originals from pre-existing variants in the input file.
    # Variants already present are reused as-is; only missing ones are generated via LLM.
    in_para = df_in[COL_IS_PARAPHRASE].apply(_is_paraphrase) if COL_IS_PARAPHRASE in df_in.columns else pd.Series(False, index=df_in.index)
    originals_df = df_in[~in_para].copy()
    # index: group_id -> list of pre-existing variant question texts (in order)
    input_variants_by_group: dict[str, list[str]] = {}
    if COL_IS_PARAPHRASE in df_in.columns and COL_GROUP_ID in df_in.columns:
        for _, vrow in df_in[in_para].iterrows():
            gid = str(vrow.get(COL_GROUP_ID, "")).strip()
            if gid:
                input_variants_by_group.setdefault(gid, []).append(str(vrow.get(COL_QUESTION, "")).strip())
    print(f"  {len(originals_df)} originals, {sum(len(v) for v in input_variants_by_group.values())} pre-existing variants in input.")

    # --- Resume: load previous output ---
    # rows is a dict keyed by q_id so upserts are safe and the file always contains
    # all previously computed responses — interruptions never cause data loss.
    already_done: set[str] = set()   # fully done (original + all variants present in output)
    original_done: set[str] = set()  # original response exists in output
    rows: dict[str, dict] = {}       # keyed by q_id; pre-seeded with all previous data
    by_qid: dict[str, dict] = {}
    if output_path.exists():
        df_prev = pd.read_excel(output_path)
        for _, r in df_prev.iterrows():
            qid = str(r.get("q_id", "")).strip()
            by_qid[qid] = r.to_dict()

        rows = dict(by_qid)
        load_full_contexts(rows, output_path)  # restore full contexts from parquet sidecar

        existing_variant_count: dict[str, int] = {}
        for r in by_qid.values():
            if _is_paraphrase(r.get("is_paraphrase")):
                gid = str(r.get("paraphrase_group_id", "")).strip()
                if gid:
                    existing_variant_count[gid] = existing_variant_count.get(gid, 0) + 1

        for qid, r in by_qid.items():
            if not _is_paraphrase(r.get("is_paraphrase")) and pd.notna(r.get("response")):
                original_done.add(qid)
                if existing_variant_count.get(qid, 0) >= n_variants:
                    already_done.add(qid)

        needs_variants = original_done - already_done
        if already_done:
            print(f"Resuming: {len(already_done)} groups fully done (skipped).")
        if needs_variants:
            print(f"          {len(needs_variants)} originals already answered but missing variants → will generate variants only.")

    # --- Filter remaining originals ---
    remaining = originals_df[~originals_df[COL_QID].astype(str).str.strip().isin(already_done)]
    if remaining.empty:
        print("All questions already answered with the requested number of variants. Nothing to do.")
        return

    # --- Init RAG pipeline ---
    needs_new_originals = not remaining[~remaining[COL_QID].astype(str).str.strip().isin(original_done)].empty
    needs_variants_rag = any(
        len(input_variants_by_group.get(str(r[COL_QID]).strip(), [])) < n_variants
        or str(r[COL_QID]).strip() not in {
            v for v in by_qid
            if _is_paraphrase(by_qid[v].get("is_paraphrase"))
        }
        for _, r in remaining.iterrows()
    ) if n_variants > 0 else False

    # Generation-only mode: preload chunks from a previous run's parquet
    parquet_chunks: dict[str, list[dict]] | None = None
    if args.contexts_parquet:
        src = Path(args.contexts_parquet)
        if not src.exists():
            raise SystemExit(f"Contexts parquet not found: {src}")
        pq_src = pd.read_parquet(src)
        for col in ("rerank_scores", "chunk_names"):
            if col not in pq_src.columns:
                raise SystemExit(
                    f"{src.name} lacks the '{col}' column — regenerate the source run "
                    f"with a prepare_eval_dataset.py version that stores it."
                )
        parquet_chunks = {}
        for _, r in pq_src.iterrows():
            texts  = json.loads(r["retrieved_contexts"])
            scores = json.loads(r["rerank_scores"])
            names  = json.loads(r["chunk_names"])
            parquet_chunks[str(r["q_id"]).strip()] = [
                {"text": t, "name": n, "rerank_score": s}
                for t, s, n in zip(texts, scores, names)
            ]
        print(f"[contexts_parquet] {len(parquet_chunks)} question contexts loaded from {src.name}"
              + (f" (threshold {args.reranker_threshold} will be applied)"
                 if args.reranker_threshold is not None else ""))

    rag: HybridRAGPipeline | None = None
    if needs_new_originals or n_variants > 0:
        print("Initialising HybridRAGPipeline...")
        rag_kwargs: dict = dict(
            collection_name=args.collection,
            candidates=args.candidates,
            top_n=args.top_n,
            ranker=args.ranker,
            reranker_model=args.reranker,
            reranker_threshold=args.reranker_threshold,
            ranking_mode=args.ranking_mode,
            rrf_k=args.rrf_k,
            search_config=[
                cfg for cfg in [
                    {"field": "embedding",    "weight": args.dense_weight, "metric": "COSINE", "params": {"nprobe": 16}},
                    {"field": "sparse_bm25",  "weight": args.bm25_weight,  "metric": "IP",     "params": {}} if args.bm25_weight > 0 else None,
                    {"field": "sparse_fermi", "weight": args.fermi_weight, "metric": "IP",     "params": {}} if args.fermi_weight > 0 else None,
                ]
                if cfg is not None
            ],
        )
        if args.llm_model_key:
            rag_kwargs["llm_model_key"] = args.llm_model_key
        rag = HybridRAGPipeline(**rag_kwargs)
        print("  Pipeline ready.")
    else:
        print("All original responses already present and no variants requested — skipping pipeline init.")

    # --- Pre-encode all query texts once (cached) ---
    # Questions and variants are immutable — compute their vectors once, reuse
    # across all future experiments (different weights, collections, LLMs).
    query_cache: _QueryEmbeddingCache | None = None
    needed_fields: set[str] = set()
    if rag is not None and not args.no_query_cache and parquet_chunks is None:
        query_cache = _QueryEmbeddingCache(Path(args.query_cache))
        needed_fields = {e["field"] for e in rag.search_config}

        # Collect all unique texts that will go through the pipeline
        _all_texts: list[str] = []
        for _, _row in remaining.iterrows():
            _q = str(_row.get(COL_QUESTION, "")).strip()
            if _q:
                _all_texts.append(_q)
            _gid = str(_row.get(COL_GROUP_ID, _row.get(COL_QID, ""))).strip()
            for _v in input_variants_by_group.get(_gid, []):
                if _v:
                    _all_texts.append(_v)
        _seen: set[str] = set()
        unique_texts = [t for t in _all_texts if not (t in _seen or _seen.add(t))]  # type: ignore[func-returns-value]

        to_encode = [t for t in unique_texts if query_cache.get(t, needed_fields) is None]
        hits = len(unique_texts) - len(to_encode)
        if to_encode:
            print(f"[query_cache] Encoding {len(to_encode)}/{len(unique_texts)} texts "
                  f"({hits} cache hits, fields: {sorted(needed_fields)})...")
            query_cache.update_batch(rag.batch_encode_queries(to_encode))
            print(f"[query_cache] Done. Cache saved to {args.query_cache}")
        else:
            print(f"[query_cache] All {len(unique_texts)} texts already cached — skipping encoding.")

    # --- Init paraphrase LLM (only if some originals are missing variants in the input) ---
    paraphrase_llm = None
    if n_variants > 0:
        needs_llm_variants = any(
            len(input_variants_by_group.get(str(r[COL_QID]).strip(), [])) < n_variants
            for _, r in remaining.iterrows()
        )
        if needs_llm_variants:
            print(f"Initialising paraphrase LLM (some originals have fewer than {n_variants} variants in input)...")
            paraphrase_llm = _build_paraphrase_llm(_PROXY_PREFIX)
            print("  Paraphrase LLM ready.")

    # --- Process questions ---
    errors = 0

    def _answer(qid: str, q_text: str) -> dict | None:
        """Answer a question: generation-only from parquet chunks, or full RAG."""
        if parquet_chunks is not None:
            chunks = parquet_chunks.get(qid)
            if chunks is None:
                tqdm.write(f"  [WARN] q_id={qid} absent from contexts parquet — skipped.")
                return None
            if args.reranker_threshold is not None:
                chunks = [
                    c for c in chunks
                    if c["rerank_score"] is not None
                    and c["rerank_score"] >= args.reranker_threshold
                ]
            try:
                return rag.ask_with_chunks(q_text, chunks)
            except Exception as exc:
                tqdm.write(f"  ERROR RAG [{qid}] '{q_text[:70]}': {exc}")
                return None
        pre = query_cache.get(q_text, needed_fields) if query_cache else None
        return _run_rag(rag, qid, q_text, precomputed=pre)

    for _, row in tqdm(remaining.iterrows(), total=len(remaining), desc="Questions"):
        q_id = str(row.get(COL_QID, "")).strip()
        question = str(row.get(COL_QUESTION, "")).strip()
        reference = str(row.get(COL_REFERENCE, "")).strip()
        ref_files = parse_reference_files(row.get(COL_REF_FILES))
        intent = str(row.get(COL_INTENT, "")).strip() if COL_INTENT in df_in.columns else ""
        group_id = str(row.get(COL_GROUP_ID, q_id)).strip() or q_id

        def _make_row(qid: str, q_text: str, is_para: bool) -> dict:
            return {
                "q_id": qid, "test_intent": intent,
                "paraphrase_group_id": group_id, "is_paraphrase": is_para,
                "user_input": q_text, "response": None,
                "reference": reference, "retrieved_contexts": None,
                "reference_files": json.dumps(ref_files, ensure_ascii=False),
                "cited_files": None,
            }

        # Original question — skip RAG if already answered, reuse existing row
        if q_id in original_done and q_id in by_qid:
            orig_row = dict(by_qid[q_id])
            orig_row["paraphrase_group_id"] = group_id
            orig_row["is_paraphrase"] = False
        else:
            orig_row = _make_row(q_id, question, False)
            if question and rag is not None:
                result = _answer(q_id, question)
                if result:
                    _fill_rag_result(orig_row, result)
                else:
                    errors += 1
        rows[q_id] = orig_row  # upsert

        # Variants: use pre-existing from input, generate missing ones with LLM
        existing_variant_qs = input_variants_by_group.get(group_id, [])
        missing = n_variants - len(existing_variant_qs)
        generated_qs: list[str] = []
        if missing > 0 and paraphrase_llm and question:
            generated_qs = _generate_paraphrases(paraphrase_llm, question, missing)
        all_variant_qs = existing_variant_qs + generated_qs

        for i, variant_q in enumerate(all_variant_qs[:n_variants], 1):
            var_id = f"{q_id}_v{i}"
            if var_id in by_qid and pd.notna(by_qid[var_id].get("response")):
                rows[var_id] = dict(by_qid[var_id])  # already has response, reuse
            else:
                var_row = _make_row(var_id, variant_q, True)
                result = _answer(var_id, variant_q) if rag is not None else None
                if result:
                    _fill_rag_result(var_row, result)
                else:
                    errors += 1
                rows[var_id] = var_row  # upsert

        # Incremental save — always contains all data; interruption can't cause data loss
        save_eval_outputs(rows, output_path)

    # --- Final stats ---
    df_out = pd.DataFrame(list(rows.values()))
    if "is_paraphrase" in df_out.columns:
        originals = df_out[df_out["is_paraphrase"] != True]
        variants = df_out[df_out["is_paraphrase"] == True]
        n_ok = originals["response"].notna().sum()
        print(f"\nDone: {n_ok}/{len(originals)} original questions answered ({errors} errors).")
        print(f"      {len(variants)} variant rows generated.")
    else:
        n_ok = df_out["response"].notna().sum()
        print(f"\nDone: {n_ok}/{len(df_out)} questions answered ({errors} errors).")
    print(f"Output: {output_path}")


if __name__ == "__main__":
    main()
